package pruebathree;

public class alex {
    private int a;
    private int b;
    public alex(int a, int b){
         this.a = a;
         this.b = b;
        
    }
    public int get(){
        return a;
        
    }
    public int getb(){
        return b;
    }
    public int suma(){
        int result = a+b;
        return result;
    }
}
